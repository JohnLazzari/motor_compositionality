def plot_kin_2d(ax, tg, xy, color, linestyle="solid", alpha=0.75):
    ax.plot(
        xy[:, 0], xy[:, 1], linewidth=4, linestyle=linestyle, alpha=alpha, color=color
    )
    ax.scatter(xy[0, 0], xy[0, 1], s=150, marker="x", color=color)
    ax.scatter(tg[0], tg[1], s=150, marker="^", color=color)


def plot_1d(pos, env, color):
    plt.plot(pos, linewidth=4, color=color)
    if env in extension_dict:
        plt.xlim([0, 150])
    elif env in retraction_dict:
        plt.xlim([0, 300])
    else:
        raise Exception
    ax = plt.gca()
    # Remove everything
    ax.axis("off")


def _to_numpy(data):
    if hasattr(data, "detach"):
        return data.detach().cpu().numpy()
    return np.asarray(data)


def _movement_time_for_condition(env_name, speed):
    if env_name in extension_dict:
        return int(np.arange(50, 150, 10)[speed])
    if env_name in retraction_dict:
        return int(np.arange(100, 300, 20)[speed])
    raise ValueError(f"Unknown environment {env_name}")


def _static_action_trial(actions, env, reach_conds, speed, delay_cond=1):
    effector = mn.effector.RigidTendonArm26(mn.muscle.MujocoHillMuscle())
    env = env(effector=effector)
    actions = torch.as_tensor(actions, dtype=torch.float32)

    options = {
        "batch_size": actions.shape[0],
        "reach_conds": torch.as_tensor(reach_conds, dtype=torch.long),
        "speed_cond": int(speed),
        "delay_cond": int(delay_cond),
        "deterministic": True,
    }
    _, info = env.reset(testing=True, options=options)

    trial_data = {"action": [], "xy": [], "tg": []}
    for timestep in range(actions.shape[1]):
        _, _, terminated, info = env.step(timestep, action=actions[:, timestep, :])
        trial_data["action"].append(actions[:, timestep : timestep + 1, :])
        trial_data["xy"].append(info["states"]["fingertip"][:, None, :])
        trial_data["tg"].append(info["goal"][:, None, :])
        if terminated:
            break

    for key in trial_data:
        trial_data[key] = torch.cat(trial_data[key], dim=1)
    trial_data["epoch_bounds"] = env.epoch_bounds
    return trial_data


def _run_kinematics_model(model, obs):
    device = next(model.parameters()).device
    obs = torch.as_tensor(obs, dtype=torch.float32, device=device)
    batch_size = obs.shape[0]
    hid_size = getattr(model, "hid_size", model.mrnn.total_num_units)
    x = torch.zeros(size=(batch_size, hid_size), device=device)
    h = torch.zeros(size=(batch_size, hid_size), device=device)

    model.eval()
    with torch.no_grad():
        _, _, outputs = model(obs, x, h, noise=False)
    return outputs.detach().cpu()


def _plot_static_action_trials(exp_path, muscle_data, action_key, direction_step):
    reach_conds = _to_numpy(muscle_data["reach_conds"])
    direction_indices = np.arange(0, len(reach_conds), direction_step)

    plt.rc("figure", figsize=(4, 4))

    for env_name, speed, delay_cond, data in tqdm.tqdm(
        iter_muscle_conditions(muscle_data), desc=f"Plotting {action_key} kinematics"
    ):
        actions = _to_numpy(data[action_key])[direction_indices]
        selected_reach_conds = reach_conds[direction_indices]
        trial_delay_cond = 1 if delay_cond is None else delay_cond

        trial_data = _static_action_trial(
            actions,
            env_dict[env_name],
            selected_reach_conds,
            int(speed),
            trial_delay_cond,
        )

        _, ax = standard_2d_ax()
        colors = plt.cm.inferno(np.linspace(0, 1, trial_data["xy"].shape[0]))
        for i, (tg, xy) in enumerate(zip(trial_data["tg"], trial_data["xy"])):
            plot_kin_2d(
                ax,
                tg[trial_data["epoch_bounds"]["movement"][0]],
                xy,
                colors[i],
            )

        suffix = f"{env_name}_speed{speed}"
        if delay_cond is not None:
            suffix = f"{suffix}_delay{delay_cond}"
        save_fig(
            os.path.join(exp_path, "scatter", f"{suffix}_kinematics"),
            eps=True,
        )


def plot_saved_action_kinematics(model_name, direction_step=4):
    """Plot arm kinematics generated by replaying saved action data."""
    model_path = f"checkpoints/{model_name}"
    exp_path = f"results/{model_name}/kinematics/saved_actions"
    muscle_data = load_pickle(os.path.join(model_path, "muscle_act_data.pkl"))
    _plot_static_action_trials(exp_path, muscle_data, "action", direction_step)


def plot_kinematics_model_outputs(kinematics_model_name, direction_step=4):
    """Temporary plot of arm kinematics generated by kinematics-model outputs."""
    model = load_supervised_model(kinematics_model_name)
    data_path = model.data_path
    if data_path is None:
        data_path = os.path.join(
            "checkpoints", kinematics_model_name, "muscle_act_data.pkl"
        )
    if not os.path.exists(data_path) and kinematics_model_name.endswith("_kinematics"):
        base_model_name = kinematics_model_name.removesuffix("_kinematics")
        data_path = os.path.join("checkpoints", base_model_name, "muscle_act_data.pkl")
    muscle_data = load_pickle(data_path)

    for _, _, _, data in iter_muscle_conditions(muscle_data):
        data["model_output"] = _run_kinematics_model(model, data["obs"])

    exp_path = f"results/{kinematics_model_name}/kinematics/model_outputs"
    _plot_static_action_trials(exp_path, muscle_data, "model_output", direction_step)


def plot_task_kinematics(model_name):
    """
    This function plots the kinematics for a model across
    all conditions

    Args:
        model_name (str): name of model
    """
    model_path = f"checkpoints/{model_name}"
    exp_path = f"results/{model_name}/kinematics"

    plt.rc("figure", figsize=(4, 4))

    test = Test(model_path, model_name)

    for env in env_dict:
        for speed in range(10):
            _, ax = standard_2d_ax()

            options = {
                "batch_size": 8,
                "reach_conds": torch.arange(0, 32, 4),
                "speed_cond": speed,
                "deterministic": True,
                "delay_cond": 1,
            }

            trial_data = test.trial(options, env_dict[env])

            colors = plt.cm.inferno(np.linspace(0, 1, trial_data["xy"].shape[0]))

            for i, (obs, xy) in enumerate(zip(trial_data["obs"], trial_data["xy"])):
                plot_kin_2d(
                    ax,
                    obs[trial_data["epoch_bounds"]["movement"][0], 12:14],
                    xy,
                    colors[i],
                )

            save_fig(
                os.path.join(exp_path, "scatter", f"{env}_speed{speed}_kinematics"),
                eps=False,
            )


def plot_speed_kinematics(model_name):
    """
    Plot kinematics across speeds in the x and y plane separately

    Args:
        model_name (str): name of model to test on
    """

    model_path = f"checkpoints/{model_name}"
    exp_path = os.path.join(
        f"results/{model_name}/kinematics", "speeds", "speed_kinematics"
    )

    test = Test(model_path, model_name)

    plt.rc("figure", figsize=(8, 4))

    colors = plt.cm.Reds(np.linspace(0, 1, 10))

    for env in env_dict:
        for speed in range(10):
            options = {
                "batch_size": 16,
                "reach_conds": torch.arange(0, 32, 2),
                "speed_cond": speed,
                "delay_cond": 1,
            }
            trial_data = test.trial(options, env_dict[env], noise=True)

            start = trial_data["epoch_bounds"]["movement"][0]
            end = trial_data["epoch_bounds"]["movement"][1]

            x_pos = trial_data["xy"][0, start:end, 0]
            y_pos = trial_data["xy"][0, start:end, 1]

            # x pos
            plot_1d(x_pos, env, colors[speed])
            save_fig(
                os.path.join(
                    exp_path,
                    f"{env}_x_kinematics_speed_{speed}",
                ),
                eps=True,
            )

            # y pos
            plot_1d(y_pos, env, colors[speed])
            save_fig(
                os.path.join(
                    exp_path,
                    f"{env}_y_kinematics_speed_{speed}",
                ),
                eps=True,
            )


